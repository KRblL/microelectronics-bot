def analogs_system_prompt(required_count: int) -> str:
    if required_count == 2:
        count_rule = "1) Верни РОВНО 2 аналога (не 1–3, а именно 2)."
        json_tail = ',\n    { "...": "второй аналог" }'
    else:
        count_rule = "1) Верни РОВНО 3 аналога (не 3–5, а именно 3)."
        json_tail = ',\n    { "...": "второй аналог" },\n    { "...": "третий аналог" }'
    return _ANALOGS_PROMPT_BODY.format(count_rule=count_rule, json_tail=json_tail)
